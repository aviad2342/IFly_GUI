    @SuppressWarnings({ "rawtypes", "unchecked" })
	private void btnShowCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowCustomerActionPerformed
        DefaultListModel modle2 = new DefaultListModel();
        try{
        selectedBranch = (String)BranchList.getSelectedValue();
       if(BranchList.getSelectedValue() == null){
            view.sound("/sounds/errorSound.wav");
            JOptionPane.showMessageDialog(null, "you must choose branch first!", "Added Message", JOptionPane.PLAIN_MESSAGE, view.icon("/images/symbolError.png"));
        }else if(view.getPotentialCustomersForBranch().get(models.get(selectedBranch)).isEmpty()){
            view.sound("/sounds/errorSound.wav");
            JOptionPane.showMessageDialog(null, "Sorry no potential customers for this branch!", "Added Message", JOptionPane.PLAIN_MESSAGE, view.icon("/images/symbolError.png"));
        }else{
        for(Customer c : view.getPotentialCustomersForBranch().get(models.get(selectedBranch))){
            modle2.addElement("Name: "+c.getFirstName()+" "+c.getLastName()+" Number: "+c.getPassportNumber());
            customer.put("Name: "+c.getFirstName()+" "+c.getLastName()+" Number: "+c.getPassportNumber(), c);
        }
        view.sound("/sounds/okSound.wav");
        CustomerList.setModel(modle2);
        }
        }catch (NullPointerException e){
            view.sound("/sounds/errorSound.wav");
            JOptionPane.showMessageDialog(null, "Sorry no branchs whith potential customers found!", "Added Message", JOptionPane.PLAIN_MESSAGE, view.icon("/images/symbolError.png"));
       }
    }//GEN-LAST:event_btnShowCustomerActionPerformed

