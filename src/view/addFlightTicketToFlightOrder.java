    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       try{
           InputTestsStaticClass.notInSystem(cbFlightNumber, "Flights");
           InputTestsStaticClass.notInSystem(cbPassportNumber, "customers");
           InputTestsStaticClass.notInSystem(cbOrderNumber, "orders");
       
        if(flg == 0 && !txtSeatNumber.getText().equals("0") && !txtRowNumber.getText().equals("0") ){
        view.addFlightTicketToFlightOrder(cbFlightNumber, cbOrderNumber, cbPassportNumber, txtSeatNumber, txtRowNumber, cbClassType, this);
        } else{
        view.sound("/sounds/errorSound.wav");
        int response = JOptionPane.showConfirmDialog(null, "You dident choose a seat! \nwould you like to try again?", "Add Error", JOptionPane.YES_NO_OPTION);
        if (response == JOptionPane.YES_OPTION) {  // reset fields
            flg = 0;
            cbFlightNumber.setSelectedIndex(0);
            cbOrderNumber.setSelectedIndex(0);
            cbPassportNumber.setSelectedIndex(0);
          } else {
             dispose();
          }
        }
       }catch (NotInTheSystemYestException ex) {
            ex.getStackTrace();
            dispose();
          }
    }//GEN-LAST:event_btnAddActionPerformed

