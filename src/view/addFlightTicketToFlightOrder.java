    @SuppressWarnings("deprecation")
	private void btnChooseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChooseActionPerformed
        try{
           InputTestsStaticClass.notInSystem(cbFlightNumber, "Flights");
           InputTestsStaticClass.notInSystem(cbPassportNumber, "customers");
           InputTestsStaticClass.notInSystem(cbOrderNumber, "orders");
        if(view.getFlights().get((int)cbFlightNumber.getSelectedItem()).getAirplane().getTotalNumberSeats() <= 200){
        Seats seats = new Seats(null, closable, myDesk, view, (int)cbFlightNumber.getSelectedItem());
        seats.show();
        try{
        if(seats.getFlg() == 0){    
        txtSeatNumber.setText(String.valueOf(seats.getSeat()));
        txtRowNumber.setText(String.valueOf(seats.getRow()));
        cbClassType.setSelectedIndex(seats.getClassType().ordinal());
        txtFee.setText(" "+String.valueOf((int)(view.getFlights().get((int)cbFlightNumber.getSelectedItem()).getFlightCost()*seats.getClassType().getFee()))+" $");
        } else {
            flg = 1;
        }
       }catch (NullPointerException e){
            System.out.printf(e.getMessage());
       }
      } else {
          SeatsOver200 seatsOver200 = new SeatsOver200(null, closable, myDesk, view, (int)cbFlightNumber.getSelectedItem());
        seatsOver200.show();
        try{
        if(seatsOver200.getFlg() == 0){    
        txtSeatNumber.setText(String.valueOf(seatsOver200.getSeat()));
        txtRowNumber.setText(String.valueOf(seatsOver200.getRow()));
        cbClassType.setSelectedIndex(seatsOver200.getClassType().ordinal());
        txtFee.setText(" "+String.valueOf((int)(view.getFlights().get((int)cbFlightNumber.getSelectedItem()).getFlightCost()*seatsOver200.getClassType().getFee()))+" $");
        } else {
            flg = 1;
        }
       }catch (NullPointerException e){
            System.out.printf(e.getMessage());
       }   
        }
        }catch (NotInTheSystemYestException ex) {
            ex.getStackTrace();
            dispose();
          }
    }//GEN-LAST:event_btnChooseActionPerformed


